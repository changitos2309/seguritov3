    private void txt_cantrabajadoresKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cantrabajadoresKeyTyped
        char validar=evt.getKeyChar();
      
      if(Character.isLetter(validar)){
          getToolkit().beep();
          evt.consume();
          
          JOptionPane.showMessageDialog(rootPane, "Ingrese solo Numeros");
      }
    }//GEN-LAST:event_txt_cantrabajadoresKeyTyped

